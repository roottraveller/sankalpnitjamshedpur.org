<p>Sankalp is a Non-Government Organization (NGO) founded by the alumni of NIT Jamshedpur. We provide free and quality education to underprivileged children living in slums and villages near the college campus. We lay emphasis on the upliftment of people in the slums by organizing free medical checkup camp and cleanliness drive.  We are a group of people with diverse interest &amp; background sharing a common fire burning within us. It is the energy of this flame which keeps us motivated to work towards improvement &amp; educational enrichment of young children of India.</p>
	<p>Currently sankalp is operational in the areas located at the outskirts of NIT JSR campus. The families residing here have their major sources of income from the industrial hub situated at Adityapur, Jamshedpur. Most of them are daily workers and as such cannot afford proper education for their children. Students of NIT Jamshedpur along with some hired professionals provide these children with elementary education in accordance to a standard syllabus. </p>
	<h1  class="heading">Vision and Mission</h1>
		  
   <p>Sankalp emerged with the vision to inspire the privileged sections of society to work for the poor and present a model that can be replicated elsewhere. 
   Through Sankalp we are spreading the light of knowledge to these children with an aim to explore their hidden talents and channelize it for the betterment of our nation by providing them with quality education. Initially we have targeted Mohan Nagar, a slum behind Hostel C, NIT Jamshedpur to achieve this purpose. Now we have three such centers across Adityapur with more than 90 active members and 230 kids benefitting from this initiative.</p>
